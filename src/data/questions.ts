export function getQuestionsBySetId(setId: string): Question[] {
  return questions
    .filter((q) => q.setId === setId)
    .sort((a, b) => a.order - b.order);
}

export function getQuestionSetById(setId: string): QuestionSet | undefined {
  return questionSets.find((s) => s.id === setId);
}
